#!/usr/bin/env bash
# =============================================================================
# FFmpeg WASM 编译配置文件
# 根据需要修改下面的参数，然后运行 ./build.sh 即可。
# =============================================================================

# -----------------------------------------------------------------------------
# 版本与来源
# -----------------------------------------------------------------------------

# FFmpeg 版本号（从官网 https://ffmpeg.org/releases/ 查看可用版本）
FFMPEG_VERSION="6.1.1"

# libx264 版本（git tag / branch，"stable" 表示最新稳定分支）
X264_VERSION="stable"

# libx265 版本（从 https://bitbucket.org/multicoreware/x265_git/downloads/ 获取）
X265_VERSION="3.5"

# Emscripten SDK 版本（与 devcontainer 保持一致；若用 CI 镜像可忽略此项）
# 仅在 CI 流程中的 mymindstorm/setup-emsdk action 里使用
EMSDK_VERSION="3.1.58"

# -----------------------------------------------------------------------------
# 编解码器开关
# 说明：DECODER 仅依赖 FFmpeg 内置软件解码器，无需外部库。
#       ENCODER 需要对应的外部静态库（libx264 / libx265），编译时间更长。
# -----------------------------------------------------------------------------

# 启用 H.264 软件解码器（播放器必需）
ENABLE_H264_DECODER=1

# 启用 H.264 编码器 libx264（播放器默认不需要编码，保持 0 可显著减小体积）
ENABLE_H264_ENCODER=0

# 启用 H.265/HEVC 软件解码器（播放器必需）
ENABLE_H265_DECODER=1

# 启用 H.265/HEVC 编码器 libx265（播放器默认不需要编码，保持 0 可显著减少编译时间）
ENABLE_H265_ENCODER=0

# 启用 AAC 音频解码器（播放器通常需要）
ENABLE_AAC=1

# 启用 MP3 音频解码器
ENABLE_MP3=1

# 启用 Opus 音频解码器（低延迟流常用；关闭可减小 wasm 体积）
ENABLE_OPUS=0

# 启用 VP8/VP9 视频编解码器（WebM 格式支持）
ENABLE_VP8_VP9=0

# -----------------------------------------------------------------------------
# 容器 / 封装格式支持（影响 wasm 体积，按需开启）
# iov-h5player 在 JS 侧解 FLV，WASM 只需解码器，默认全部关闭。
# -----------------------------------------------------------------------------

# MP4 / MOV 容器（H.264/H.265 最常见封装）
ENABLE_FMT_MP4=0

# Matroska / WebM 容器（MKV、WebM）
ENABLE_FMT_MKV=0

# FLV 容器（直播流常用；播放器在 JS 解复用，无需 FFmpeg demuxer）
ENABLE_FMT_FLV=0

# HLS / TS 流（HTTP Live Streaming）
ENABLE_FMT_HLS=0

# MPEG-PS / MPEG-TS
ENABLE_FMT_MPEGTS=0

# -----------------------------------------------------------------------------
# 网络协议支持
# 注意：开启网络协议会增加 wasm 体积，且在浏览器安全策略下部分协议不可用。
#       通常 H5 播放器直接通过 JS Fetch/XHR 下载数据，不需要 FFmpeg 网络协议。
# -----------------------------------------------------------------------------

# 启用 HTTP/HTTPS 协议（一般由 JS 侧负责网络，建议关闭以减小体积）
ENABLE_NETWORK=0

# -----------------------------------------------------------------------------
# WASM 内存设置
# 浏览器限制：单 wasm 实例最大 4 GB (32-bit addr space)
# -----------------------------------------------------------------------------

# 初始内存分配大小（字节），推荐 32~64 MB
# 必须是 65536 (64KB) 的整数倍
INITIAL_MEMORY=$((32 * 1024 * 1024))   # 32 MB

# 允许的最大内存（字节）
# 设置较大值可避免处理大文件时 OOM，但实际用多少取决于运行时
MAXIMUM_MEMORY=$((2 * 1024 * 1024 * 1024))  # 2 GB

# 是否允许内存动态增长（推荐开启，配合 MAXIMUM_MEMORY 使用）
ALLOW_MEMORY_GROWTH=1

# -----------------------------------------------------------------------------
# WASM 线程模型
#
# ENABLE_WASM_THREADS=0（默认，推荐）：
#   单线程 WASM，不启用 Emscripten pthread。
#   不需要 COOP/COEP / crossOriginIsolated，可用 http://IP 直接访问。
#   解码在 Web Worker 中运行，不阻塞主线程。
#   仅链接 iov_decoder + libavcodec/libavutil/swresample/swscale，体积更小。
#
# ENABLE_WASM_THREADS=1：
#   pthread 版 WASM，需要页面 cross-origin isolated（COOP/COEP 或 HTTPS）。
#   链接 ffmpeg CLI (fftools)，体积更大。
# -----------------------------------------------------------------------------

ENABLE_WASM_THREADS=0

# 是否链接 ffmpeg 命令行 (fftools)。播放器仅需 iov_decoder 时保持 0。
ENABLE_FFMPEG_CLI=0

# 以下仅在 ENABLE_WASM_THREADS=1 时生效
ENABLE_THREADS=0
PTHREAD_POOL_SIZE=4

# -----------------------------------------------------------------------------
# SIMD 优化（实验性）
# 可提升解码性能，需要浏览器支持 WebAssembly SIMD（Chrome 91+、Firefox 89+）
# -----------------------------------------------------------------------------

ENABLE_SIMD=0

# -----------------------------------------------------------------------------
# 调试模式
# 开启后：wasm 文件较大、包含 Source Map；关闭后体积更小、性能更好。
# -----------------------------------------------------------------------------

ENABLE_DEBUG=0

# 优先减小 wasm 体积（-Oz）；关闭则使用 -O3 略快但更大
ENABLE_SIZE_OPTIMIZE=1

# -----------------------------------------------------------------------------
# 输出设置
# -----------------------------------------------------------------------------

# 最终产物输出目录（相对于项目根目录）
OUTPUT_DIR="./output"

# 输出 JS 文件名（会同时生成 .wasm 文件）
OUTPUT_NAME="ffmpeg-core"

# JS 模块导出名称（JS 中通过 createFFmpegCore() 初始化实例）
EXPORT_NAME="createFFmpegCore"

# 是否使用 ES Module 格式输出（0 = CommonJS / IIFE，1 = ESM）
# 注：ESM 需要 <script type="module"> 或打包工具支持
USE_ES6_MODULE=0

# -----------------------------------------------------------------------------
# 编译并行度
# 设为 0 时自动使用 $(nproc) 检测 CPU 核心数
# -----------------------------------------------------------------------------

MAKE_JOBS=0

# -----------------------------------------------------------------------------
# 缓存目录（依赖库编译结果缓存，加速重复构建）
# -----------------------------------------------------------------------------

BUILD_CACHE_DIR="${HOME}/.build-cache/ffmpeg-wasm"
